let a = 5;
let b = 7;

console.log(a + b, 'adición');//adición
console.log(a - b, 'resta');//resta
console.log(a * b, 'multiplicación');//multiplicación
console.log(a / b, 'división');//división
console.log(a % b, 'módulo');//módulo
console.log(a ** b, 'potencia');//potencia

//incrementar
console.log(++a);//suma 1 y lo muestra
console.log(a++);//suma 1 pero después hay que imprimirlo para que lo muestre
console.log(a);

//decrementar
console.log(--a);//resta 1 y lo muestra
console.log(a--);//resta 1 pero después hay que imprimirlo para que lo muestre
console.log(a);
